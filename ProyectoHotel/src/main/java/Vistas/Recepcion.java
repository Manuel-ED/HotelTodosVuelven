public void actualizarVistaRecepcion() {
    Cliente siguiente = hotel.getRecepcionManager().verSiguiente();
    if (siguiente != null) {
        jLabel5.setText(siguiente.getNombre() + " (DNI: " + siguiente.getDni() + ")");
    } else {
        jLabel5.setText("--- Sin Clientes en Espera ---");
    }
    
    
    modeloFilaEspera.setRowCount(0); 
    
    ListaEnlazada<Cliente> listaFila = hotel.getRecepcionManager().getFilaEsperaClientes().obtenerElementosComoLista();
    
    int contador = 1;
    Nodo<Cliente> actual = listaFila.getCabeza();
    while (actual != null) {
        Cliente c = actual.getDato();
        
        modeloFilaEspera.addRow(new Object[]{
    contador++, 
    c.getNombre(), 
    c.getDni(), 
    c.getHoraLlegada()
});
    }

}

