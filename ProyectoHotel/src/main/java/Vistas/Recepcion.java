    private void btnAtenderSiguienteClienteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAtenderSiguienteClienteActionPerformed
    Cliente clienteAtendido = hotel.getRecepcionManager().atenderSiguienteCliente();
    
    if (clienteAtendido != null) {
        JOptionPane.showMessageDialog(this, 
            "Atendiendo a: " + clienteAtendido.getNombre() + ". Procede al Check-in.", 
            "Atención Cliente", JOptionPane.INFORMATION_MESSAGE);
        
        actualizarVistaRecepcion();
        
    } else {
        JOptionPane.showMessageDialog(this, "No hay clientes esperando en la fila de recepción.", "Información", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_btnAtenderSiguienteClienteActionPerformed

